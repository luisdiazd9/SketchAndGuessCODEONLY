    private void botonListoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonListoActionPerformed
        // TODO add your handling code here:
        Usuario.idEnPartida = partida.usuarioListo(Usuario.region, Usuario.nombreUsuario, Usuario.puntajeMaximo);
        if (Usuario.idEnPartida == 100) {
            System.out.println("Error");
        } else if (Usuario.idEnPartida == 9) {
            JOptionPane.showMessageDialog(
                    f,
                    "La partida ya empezó");
        } else {
            if (Usuario.idEnPartida == 0) { //Es el primer usuario en entrar
                //PRIMERO
                botonListo.setVisible(false);
                botonEmpezar.setVisible(true);
                lblActualizar.setVisible(true);
            } else { //Es otro usuario
                //OTRO
                botonListo.setVisible(false);
                jLabelEspera.setVisible(true);
                lblActualizar.setVisible(true);
            }
            actualizarDatoslbl();
        }
    }//GEN-LAST:event_botonListoActionPerformed

    private void botonEmpezarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEmpezarActionPerformed
        // TODO add your handling code here:
        usuariosTodos = partida.obtenerUsuarios(Usuario.region);
        if (usuariosTodos[1] != null) { //Valida que haya al menos dos usuarios
            partida.empiezanPartida(Usuario.region);
            this.setVisible(false);
            vistaEscribirPalabra vistaPalabra = new vistaEscribirPalabra();
            vistaPalabra.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(
                    f,
                    "Recuerda que deben ser al menos dos jugadores");
        }

    }//GEN-LAST:event_botonEmpezarActionPerformed

