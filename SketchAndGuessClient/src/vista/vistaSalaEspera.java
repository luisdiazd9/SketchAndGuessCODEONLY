    private void obtenerDatosInicial() {

        jLabelUsuario2.setText(null);
        jLabelUsuario3.setText(null);
        jLabelUsuario4.setText(null);
        jLabelUsuario5.setText(null);
        usuariosTodos[0] = null;
        usuariosTodos[1] = null;
        usuariosTodos[2] = null;
        usuariosTodos[3] = null;
        usuariosTodos[4] = null;
        usuariosTodos[5] = null;
    }

    private void actualizarDatoslbl() {
        usuariosTodos = partida.obtenerUsuarios(Usuario.region);
        if (usuariosTodos[4] != null && usuariosTodos[5] == null) {//Ya están todos los usuarios, pero aún no empieza la partida
            partidaHasStarted = 2;
        } else if (usuariosTodos[5] != null) { //La partida ya empezó
            partidaHasStarted = 1;
            jLabelEspera.setText("¡Buena suerte!");
            vistaEscribirPalabra vistaPalabra = new vistaEscribirPalabra();
            this.setVisible(false);
            vistaPalabra.setVisible(true);
        } else {
            switch (Usuario.idEnPartida) {
                case 0:
                    jLabelUsuario2.setText(usuariosTodos[1]);
                    jLabelUsuario3.setText(usuariosTodos[2]);
                    jLabelUsuario4.setText(usuariosTodos[3]);
                    jLabelUsuario5.setText(usuariosTodos[4]);
                    break;
                case 1:
                    jLabelUsuario2.setText(usuariosTodos[0]);
                    jLabelUsuario3.setText(usuariosTodos[2]);
                    jLabelUsuario4.setText(usuariosTodos[3]);
                    jLabelUsuario5.setText(usuariosTodos[4]);
                    break;
                case 2:
                    jLabelUsuario2.setText(usuariosTodos[1]);
                    jLabelUsuario3.setText(usuariosTodos[0]);
                    jLabelUsuario4.setText(usuariosTodos[3]);
                    jLabelUsuario5.setText(usuariosTodos[4]);
                    break;
                case 3:
                    jLabelUsuario2.setText(usuariosTodos[1]);
                    jLabelUsuario3.setText(usuariosTodos[2]);
                    jLabelUsuario4.setText(usuariosTodos[0]);
                    jLabelUsuario5.setText(usuariosTodos[4]);
                    break;
                case 4:
                    jLabelUsuario2.setText(usuariosTodos[1]);
                    jLabelUsuario3.setText(usuariosTodos[2]);
                    jLabelUsuario4.setText(usuariosTodos[3]);
                    jLabelUsuario5.setText(usuariosTodos[0]);
                    break;
            }
        }
    }

