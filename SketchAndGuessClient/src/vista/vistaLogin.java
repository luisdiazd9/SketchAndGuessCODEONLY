    private void jlabelTextoRegistro2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlabelTextoRegistro2MouseClicked
        // TODO add your handling code here:
        int region = 1;
        String usuario = JOptionPane.showInputDialog(
                f,
                "Digita el nombre de usuario",
                "Nuevo usuario",
                JOptionPane.QUESTION_MESSAGE);
        String clave = JOptionPane.showInputDialog(
                f,
                "Digita tu contraseña",
                "Nuevo usuario",
                JOptionPane.NO_OPTION);
        String claveConf = JOptionPane.showInputDialog(
                f,
                "Confirma tu contraseña",
                "Nuevo usuario",
                JOptionPane.NO_OPTION);
        if (clave.equals(claveConf)) {
            Object seleccion = JOptionPane.showInputDialog(
                    f,
                    "Selecciona tu región",
                    "Nuevo usuario",
                    JOptionPane.QUESTION_MESSAGE,
                    null, // null para icono defecto
                    new Object[]{"Colombia", "México", "Chile"},
                    "Colombia");
            if (seleccion.equals("Colombia")) {
                region = 1;
            } else if (seleccion.equals("Chile")) {
                region = 3;
            } else {
                region = 2;
            }
            try {
                acceso = partida.registro(usuario, clave, region);
                if (acceso == 0) { //Registro satisfactorio 
                    usuarioActual.setNombreUsuario(usuario);
                    usuarioActual.setRegion(region);
                    int[] datosUs = partida.datosUsuario(usuario);
                    usuarioActual.setIdUsuario(datosUs[0]);
                    usuarioActual.setTipoUsuario(datosUs[1]);
                    usuarioActual.setPuntajeMaximo(datosUs[2]);
                    JOptionPane.showMessageDialog(
                            f,
                            "Registro satisfactorio");
                } else {//Error en registro
                    f = new JFrame();
                    JOptionPane.showMessageDialog(f, "El usuario ya existe", "Error", JOptionPane.WARNING_MESSAGE);
                }
            } catch (HeadlessException ex) {
                Logger.getLogger(vistaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(
                    f,
                    "Las contraseñas no coinciden");
        }
    }//GEN-LAST:event_jlabelTextoRegistro2MouseClicked

