    private void botonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIngresarActionPerformed
        // TODO add your handling code here:
        try {
            String usuario = jTextUsuario.getText();
            String clave = jPasswordClave.getText();
            int region;
            if (jComboBox1.getSelectedItem().equals("Colombia")) {
                region = 1;
            } else if (jComboBox1.getSelectedItem().equals("Chile")) {
                region = 3;
            } else {
                region = 2;
            }
            acceso = partida.login(usuario, clave, region);
            switch (acceso) {
                case 0:
                    //Login satisfactorio
                    usuarioActual.setNombreUsuario(usuario);
                    usuarioActual.setRegion(region);
                    int[] datosUs = partida.datosUsuario(usuario);
                    usuarioActual.setIdUsuario(datosUs[0]);
                    usuarioActual.setTipoUsuario(datosUs[1]);
                    usuarioActual.setPuntajeMaximo(datosUs[2]);
                    if (Usuario.tipoUsuario == 0) { //El usuario es admin
                        vistaAdministrador admin = new vistaAdministrador();
                        admin.setVisible(true);
                        this.setVisible(false);
                    } else { //Es usuario común
                        vistaSalaEspera sala = new vistaSalaEspera();
                        sala.setVisible(true);
                        this.setVisible(false);
                    }
                    break;
                //Región llena
                case 2:
                    JOptionPane.showMessageDialog(f, "¡Región llena, intenta con otra!", "Error", JOptionPane.WARNING_MESSAGE);
                    break;
                default:
                    //Error en login
                    JOptionPane.showMessageDialog(f, "Usuario y/o contraseña incorrectos", "Error", JOptionPane.WARNING_MESSAGE);
                    break;
            }
        } catch (HeadlessException ex) {
            Logger.getLogger(vistaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonIngresarActionPerformed

